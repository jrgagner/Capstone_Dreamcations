# User side 
# (15 points) As a traveler I want to be able to retrieve a list of vacation choices based on rainfall, temperature, country, continent, and preferred vacation time. (Meteostate API, Google maps API)
# (10 points) As an eco-friendly traveler, I want to be able to see the carbon footprint of each trip.  (Carbon Interface API)
# (10 points) As a traveler, I want to be able to add vacation spots to a list and then see that list’s locations displayed on a map. (Google maps API)
# (2.5 points) As a traveler, I want to be able to delete vacation favorites from my list. 
# (2.5 points) As a traveler, I want to be able to restart my search with new parameters. 



# Developer Side 
